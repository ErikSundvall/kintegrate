describe('Sample triage logic - autogenerated dependency tests', () => {
  it('Gestational age is visible only when pregnant is true', () => {
    cy.visit('/form-viewer.html?testMode=1&autoLoad=0');
    cy.formViewerReady();

    cy.fillField("/content[openEHR-EHR-SECTION.adhoc.v1,'Pregnancy']/items[at0001]/value", false);
    cy.expectHidden("/content[openEHR-EHR-SECTION.adhoc.v1,'Pregnancy']/items[at0002]/value");

    cy.fillField("/content[openEHR-EHR-SECTION.adhoc.v1,'Pregnancy']/items[at0001]/value", true);
    cy.expectVisible("/content[openEHR-EHR-SECTION.adhoc.v1,'Pregnancy']/items[at0002]/value");
  });
});
