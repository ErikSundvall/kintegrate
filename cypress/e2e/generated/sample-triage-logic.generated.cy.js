describe('Sample triage logic - autogenerated dependency tests', () => {
  it('sample_triage_logic/gestational_age is shown when sample_triage_logic/pregnant is true', () => {
    cy.visit('/form-viewer.html?testMode=1&autoLoad=0');
    cy.formViewerReady();

    cy.fillField("sample_triage_logic/pregnant", false);
    cy.expectHidden("sample_triage_logic/gestational_age");

    cy.fillField("sample_triage_logic/pregnant", true);
    cy.expectVisible("sample_triage_logic/gestational_age");
  });
});
